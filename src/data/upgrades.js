// effect kinds:
//   multSacrifice    : +N faith per sacrifice (additive)
//   multSacrificeMul : ×N faith per sacrifice (multiplicative)
//   multMission      : +N% to money/knowledge from missions of `category`
//   addMaxHp         : flat addition to max HP per level
//   addMaxHpPct      : multiplicative max HP per level
//   addHpRegen       : flat HP/s per level
//   addPassiveFaith  : faith per second per level
//   addPassiveMoney  : money per second per level
//   addPassiveKnowledge : knowledge per second per level
//   addPassiveAll    : adds to all three idle streams
//   multPassiveAll   : ×N to all idle streams (multiplicative)
//   reduceMissionCd  : ×N to mission duration (lower is faster)
//   multAll*Mul      : ×N multiplicative resource-scope upgrades (Devotion)
//   multMissionRewardMul : ×N multiplicative mission rewards
